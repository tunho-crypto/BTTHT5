-- Tạo bảng DMKHOA (Danh mục khoa)
CREATE TABLE DMKHOA (
    MAKHOA  CHAR(2)         NOT NULL,
    TENKHOA NVARCHAR2(30),
    CONSTRAINT PK_DMKHOA PRIMARY KEY (MAKHOA)
);
 
-- Tạo bảng DMMH (Danh mục môn học)
CREATE TABLE DMMH (
    MAMH    CHAR(2)         NOT NULL,
    TENMH   NVARCHAR2(35),
    SOTIET  NUMBER(3),
    CONSTRAINT PK_DMMH PRIMARY KEY (MAMH)
);
 
-- Tạo bảng DMSV (Danh mục sinh viên)
CREATE TABLE DMSV (
    MASV        CHAR(3)         NOT NULL,
    HOSV        NVARCHAR2(30),
    TENSV       NVARCHAR2(10),
    PHAI        NVARCHAR2(3),
    NGAYSINH    DATE,
    NOISINH     NVARCHAR2(25),
    MAKH        CHAR(2),
    HOCBONG     NUMBER(10,0),
    CONSTRAINT PK_DMSV PRIMARY KEY (MASV),
    CONSTRAINT FK_DMSV_DMKHOA FOREIGN KEY (MAKH) REFERENCES DMKHOA(MAKHOA)
);
 
-- Tạo bảng KETQUA (Kết quả thi)
CREATE TABLE KETQUA (
    MASV    CHAR(3)     NOT NULL,
    MAMH    CHAR(2)     NOT NULL,
    DIEM    NUMBER(4,2),
    CONSTRAINT PK_KETQUA PRIMARY KEY (MASV, MAMH),
    CONSTRAINT FK_KETQUA_DMSV FOREIGN KEY (MASV) REFERENCES DMSV(MASV),
    CONSTRAINT FK_KETQUA_DMMH FOREIGN KEY (MAMH) REFERENCES DMMH(MAMH)
);

